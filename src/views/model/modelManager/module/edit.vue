<template>
  <div>
    <el-button :disabled="data.id === 1" size="mini" type="success" @click="to">编辑</el-button>
    <eForm ref="form" :sup_this="sup_this" :is-add="false"/>
  </div>
</template>
<script>
import eForm from './form'
export default {
  components: { eForm },
  props: {
    data: {
      type: Object,
      required: true
    },
    // index.vue 的this 可用于刷新数据
    sup_this: {
      type: Object,
      required: true
    }
  },
  methods: {
    to() {
      const _this = this.$refs.form
      _this.form = {id:this.data.id, groupName: this.data.groupName, level: this.data.level, quota: this.data.quota, rankReward: this.data.rankReward,cpAmount: this.data.cpAmount,rankReward: this.data.rankReward,recommendedNum: this.data.recommendedNum,totalOrder: this.data.totalOrder,groupAccountNum: this.data.groupAccountNum }
      _this.dialog = true
    }
  }
}
</script>

<style scoped>
  div{display: inline-block;margin-right: 3px;}
</style>
